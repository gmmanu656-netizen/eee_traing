public class Problem1 {
    static void main() {
        int a = 10;
        int b = 20;
        System.out.println(++a + ++b);
        for (int i=1; i<5; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        for (int i=1; i<5; i++){
            System.out.print(i + " ");
        }
    }
}

